#print number of element in a sequences or list
for i in [0,1,2,3,4,5]:
    print(i)
print("---------------------------")

#print elements in a list 
menu = ["Sushi","Banhmi","Pho","Bo Vien" ]
for food in menu:
    print(food)
print("---------------------------")

#print char in a string
name ="Pho tai"
for character in name:
    print(character)
print("---------------------------")

#print in range(stop) --> includes whole number up to stop (not include stop)

for x in range(4):
    print(x)
print("---------------------------")

#print in range (start, stop) --> includes start, exclude stop
for x in range(3,7):
    print(x)
print("---------------------------")

#print in range(start,stop,step) --> includes start, increment by 2, exclude stop
for x in range(0,10,2):
    print(x)
print("---------------------------") 
